# Aufgabe 3 - Bit-Operatoren
num_1 = 42  # 101010 in Binär
num_2 = 36  # 100100 in Binär
print(num_1 & num_2)  # Bitweises UND (100000)
print(num_1 | num_2)  # Bitweises ODER (101110)
print(num_1 ^ num_2)  # Bitweises XOR (001110)
print(~num_1)  # Bitweise Negation von num_1 (-101011)
print(~num_2)  # Bitweise Negation von num_2 (-100101)