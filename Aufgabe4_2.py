# Beispiel für Rechtsverschiebung
num = 8  # Binärdarstellung: 1000
print("Ursprüngliche Zahl:", num)

# Rechtsverschiebung um 2 Bits
num >>= 2  # Nach der Verschiebung: 0010
print("Nach Rechtsverschiebung um 2 Bits:", num)


# Beispiel für Linksverschiebung
num = 4  # Binärdarstellung: 0100
print("Ursprüngliche Zahl:", num)

# Linksverschiebung um 2 Bits
num <<= 2  # Nach der Verschiebung: 10000
print("Nach Linksverschiebung um 2 Bits:", num)
